func main() {
	flag.Parse()
	log.SetFlags(0)

	hub := newHub()
	go hub.run()

	http.HandleFunc("/", func(w http.ResponseWriter, r *http.Request) {
		handler(hub, w, r)
	})
	log.Fatal(http.ListenAndServe(*addr, nil))
}
